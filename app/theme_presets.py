def get_presets_for_template(template_name):
    """Get available presets for a specific template"""
    return THEME_PRESETS.get(template_name, {})

def get_preset_config(template_name, preset_key):
    """Get configuration for a specific preset"""
    presets = get_presets_for_template(template_name)
    return presets.get(preset_key, {})

def get_all_templates():
    """Get all available template names"""
    return list(THEME_PRESETS.keys())